function populateDistricts() {
    let division = document.getElementById("division").value;
    let districtSelect = document.getElementById("district");
    let upazilaSelect = document.getElementById("upazila");
    let hospitalSelect = document.getElementById("hospital");

    districtSelect.innerHTML = '<option value="" disabled selected>Select District</option>';
    upazilaSelect.innerHTML = '<option value="" disabled selected>Select Upazila</option>';
    hospitalSelect.innerHTML = '<option value="" disabled selected>Select Hospital</option>';

    if (locationData[division]) {
        Object.keys(locationData[division]).forEach(district => {
            districtSelect.appendChild(new Option(district, district));
        });
    }
}

function populateUpazilas() {
    let division = document.getElementById("division").value;
    let district = document.getElementById("district").value;
    let upazilaSelect = document.getElementById("upazila");
    let hospitalSelect = document.getElementById("hospital");

    upazilaSelect.innerHTML = '<option value="" disabled selected>Select Upazila</option>';
    hospitalSelect.innerHTML = '<option value="" disabled selected>Select Hospital</option>';

    if (locationData[division] && locationData[division][district]) {
        Object.keys(locationData[division][district]).forEach(upazila => {
            upazilaSelect.appendChild(new Option(upazila, upazila));
        });
    }
}
function populateHospitals() {
    let division = document.getElementById("division").value;
    let district = document.getElementById("district").value;
    let upazila = document.getElementById("upazila").value;
    let hospitalSelect = document.getElementById("hospital");

    hospitalSelect.innerHTML = '<option value="" disabled selected>Select Hospital</option>';

    if (
        locationData[division] &&
        locationData[division][district] &&
        locationData[division][district][upazila]
    ) {
        let hospitals = locationData[division][district][upazila];

        if (hospitals.length > 0) {
            hospitals.forEach(hospital => {
                let option = new Option(hospital, hospital);
                hospitalSelect.appendChild(option);
            });
        } else {
            hospitalSelect.innerHTML = '<option value="" disabled>No hospitals available</option>';
        }
    } else {
        hospitalSelect.innerHTML = '<option value="" disabled>No hospitals found</option>';
    }
}
